cluster_col = "KMeans_Cluster" if algorithm == "KMeans" else "DBSCAN_Cluster"

st.subheader("ðŸ“Š Cluster Distribution")
st.bar_chart(df[cluster_col].value_counts())

st.subheader("ðŸŒ€ PCA Scatter Plot")
fig, ax = plt.subplots()
sns.scatterplot(data=df, x="PCA1", y="PCA2", hue=cluster_col, palette="tab10", ax=ax)
st.pyplot(fig)

st.subheader("ðŸ“ˆ Cluster KPIs")
st.dataframe(df.groupby(cluster_col)[["Income", "Age", "TotalSpend"]].mean())

